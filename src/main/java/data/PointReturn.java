package data;

/**
 * Holds all return information for a Player
 */

public class PointReturn {

    private int returnWinner;
    private int strong;
    private int ip;
    private int weak;
    private int winner;

    public int getReturnWinner() {
        return this.returnWinner;
    }

    public int getStrongReturn() {
        return this.strong;
    }

    public int getIP() {
        return this.ip;
    }

    public int getWeakReturn() {
        return this.weak;
    }

    public int getWinner() {
        return this.winner;
    }
}